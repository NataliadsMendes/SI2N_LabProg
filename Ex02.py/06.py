# Exercício 06

# Programa que calcula o peso ideal, usando a fórmula: (72.7*altura) - 58

# Entrada
al = float(input("Digite a sua altura (em metros): "))    # O usuário digita a sua altura

# Processamento
pe = (72.7 * al) - 58                        # Fórmula do peso ideal

# Saída
print("O seu peso ideal é:", pe, "kg")