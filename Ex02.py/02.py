# Exercício 02

# Calcular o salário total por meio do valor da hora e sua quantidade

# Entrada
hor = float(input("Digite a quantidade de horas trabalhadas: "))  # O usuário insere a quantidade de horas que trabalhou
val = float(input("Digite o valor que ganha por hora: "))         # O usuário insere o valor quee ganha por hora

# Processamento
sal = hor * val                                                   # Calcula o valor do salário final

# Saída
print("O seu salário final equivale a:", sal)                     # Mostra o resultado