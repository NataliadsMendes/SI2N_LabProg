# Exercício 04

# Conversor de temperatura:
# O usuário insire em Fahrenheit, transforme e mostre a temperatura em celsius

# Entrada
fah = float(input("Digite a temperatura em Fahrenheit: "))

# Processamento
Cel = 5 * ((fah - 32) / 9)

# Saída
print("A temperatura em celsius é:", Cel, "ºC")